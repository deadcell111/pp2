"""Example
Create a List:"""

thislist = ["apple", "banana", "cherry"]
print(thislist)

"""Example
Lists allow duplicate values:"""

thislist = ["apple", "banana", "cherry", "apple", "cherry"]
print(thislist)

"""Example
Print the number of items in the list:"""

thislist = ["apple", "banana", "cherry"]
print(len(thislist))

"""Example
String, int and boolean data types:"""

